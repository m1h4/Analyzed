Color::Color(void)
{
}

Color::Color(float a,float r,float g,float b)
{
	Set(a,r,g,b);
}

Color::Color(float r,float g,float b)
{
	Set(r,g,b);
}

Color::Color(float a,float value)
{
	Set(a,value);
}

Color::Color(float value)
{
	Set(value);
}

Color::Color(const Color& other)
{
	Set(other);
}

Color::Color(float a,const Color& other)
{
	Set(a,other);
}

Color::Color(const float* m)
{
	Set(m);
}

void Color::Set(float a,float r,float g,float b)
{
	this->a = a;
	this->r = r;
	this->g = g;
	this->b = b;
}

void Color::Set(float r,float g,float b)
{
	this->a = 1.0f;
	
	this->r = r;
	this->g = g;
	this->b = b;
}

void Color::Set(float a,float value)
{
	this->a = a;
	
	r = g = b = value;
}

void Color::Set(float value)
{
	a = r = g = b = value;
}

void Color::Set(const Color& other)
{
	a = other.a;
	r = other.r;
	g = other.g;
	b = other.b;
}

void Color::Set(float a,const Color& other)
{
	this->a = a;

	r = other.r;
	g = other.g;
	b = other.b;
}

void Color::Set(const float* m)
{
	this->m[0] = m[0];
	this->m[1] = m[1];
	this->m[2] = m[2];
	this->m[3] = m[3];
}

void Color::SetEmpty(void)
{
	a = r = g = b = 0.0f;
}

bool Color::Empty(void)
{
	return a == 0.0f && r == 0.0f && g == 0.0f && b == 0.0f;
}

bool Color::operator==(const Color& other) const
{
	return a == other.a && r == other.r && g == other.g && b == other.b;
}

bool Color::operator!=(const Color& other) const
{
	return a != other.a && r != other.r && g != other.g && b != other.b;
}

bool Color::operator<(const Color& other) const
{
	return a < other.a && r < other.r && g < other.g && b < other.b;
}

bool Color::operator>(const Color& other) const
{
	return a > other.a && r > other.r && g > other.g && b > other.b;
}

bool Color::operator<=(const Color& other) const
{
	return a <= other.a && r <= other.r && g <= other.g && b <= other.b;
}

bool Color::operator>=(const Color& other) const
{
	return a >= other.a && r >= other.r && g >= other.g && b >= other.b;
}

void Color::Clamp(float mn,float mx)
{
	a = min(max(a,mn),mx);
	r = min(max(r,mn),mx);
	g = min(max(g,mn),mx);
	b = min(max(b,mn),mx);
}

bool Color::Clamped(void) const
{
	return a > 1.0f || a < 0.0f || r > 1.0f || r < 0.0f || g > 1.0f || g < 0.0f || b > 1.0f || b < 0.0f;
}

void Color::LinearInterp(const Color& dest,float s)
{
	a = LinearInterpolation(a,dest.a,s);
	r = LinearInterpolation(r,dest.r,s);
	g = LinearInterpolation(g,dest.g,s);
	b = LinearInterpolation(b,dest.b,s);
}

void Color::CubicInterp(const Color& dest,float s)
{
	a = CubicInterpolation(a,dest.a,s);
	r = CubicInterpolation(r,dest.r,s);
	g = CubicInterpolation(g,dest.g,s);
	b = CubicInterpolation(b,dest.b,s);
}

void Color::CosInterp(const Color& dest,float s)
{
	a = CosInterpolation(a,dest.a,s);
	r = CosInterpolation(r,dest.r,s);
	g = CosInterpolation(g,dest.g,s);
	b = CosInterpolation(b,dest.b,s);
}

void Color::Brighten(float s)
{
	LinearInterp(Color(a,1.0f,1.0f,1.0f),s);
}

void Color::Darken(float s)
{
	LinearInterp(Color(a,0.0f,0.0f,0.0f),s);
}

void Color::Saturation(float s)
{
	float v = (r + g + b) / 3.0f;
	
	LinearInterp(Color(a,v,v,v),s);
}

void Color::Negative(void)
{
	r = 1.0f - r;
	g = 1.0f - g;
	b = 1.0f - b;
}

void Color::Grayscale(void)
{
	float v = (r + g + b) / 3.0f;

	r = g = b = v;
}

void Color::Shadow(void)
{
	a *= 0.1f;

	r = g = b = 0.0f;
}

Color::operator unsigned long(void) const
{
	return (unsigned long)(((((unsigned long)((a)*255.0f))&0xFF)<<24)|((((unsigned long)((r)*255.0f))&0xFF)<<16)|((((unsigned long)((g)*255.0f))&0xFF)<<8)|(((unsigned long)((b)*255.0f))&0xFF));
}

